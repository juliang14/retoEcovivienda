                    <div id="step-11" class="tab-pane" role="tabpanel" aria-labelledby="step-11">
                    <h3>Historia</h3>
                    Luego de 12 años de incertidumbre y bajo la premisa de convertir a Tunja en Una Nueva Capital, nace el
                    proyecto de vivienda de interés social (VIS) y de vivienda de interés prioritaria (VIP) Bellavista como una
                    alternativa de solución para las familias pertenecientes al grupo 3 de priorización de los proyectos
                    inconclusos Torres del Parque y Estancia del Roble y para los tunjanos que no cuentan con vivienda propia.
                    </div>
                    <div id="step-12" class="tab-pane" role="tabpanel" aria-labelledby="step-12">
                    <h3>2021</h3>
                    <div>
                        <p>El 18 de noviembre de 2021, el Consorcio Plan de Vivienda Tunja VIP - VIS, empezó la comercialización del
                        proyecto de vivienda de interés prioritario y vivienda de interés social Bellavista, con la realización de
                        una feria comercial, exclusiva para los damnificados de los antiguos proyectos de vivienda Torres del
                        Parque y Estancia del Roble caracterizados en el grupo 3 de damnificados.
                        En ese evento se socializaron el tipo de unidades de vivienda a ofertar, el valor de ventas, el cierre
                        financiero y el cronograma de trabajo para la entrega del material de los inmuebles. </p>
                        <p>El 11 de diciembre de 2021, el Consorcio Plan Vivienda Tunja VIP - VIS, puso a disposición de 579
                        familias caracterizadas en el grupo 3 de damnificados, una oficina ubicada en la calle 20 # 8-15 de Tunja,
                        tres asesoras comerciales y dos asesores bancarios para la atención y asesoramiento del proceso de compra
                        de los apartamentos ofertados, bajo la figura de comercialización prioritaria. </p>
                    </div>
                    </div>
                    <div id="step-13" class="tab-pane" role="tabpanel" aria-labelledby="step-13">
                    <h3>Trimestre 1</h3>
                    <div>
                        <p>El 15 de diciembre de 2021, se expidieron 579 cartas de intención de compra para ser entregadas a las 579
                        familias priorizadas, respecto de las cuales, se estableció un plazo de 3 meses a partir de la fecha
                        enunciada, para acercarse a la constructora a radicar los documentos que acreditaran su cierre financiero
                        y
                        pudiesen empezar el trámite de vinculación como optantes compradores.</p>
                        Como producto de la comercialización prioritaria, con corte al 15 de marzo del 2022, se tienen:
                        <br />
                        -60 clientes con formulario de vinculación firmado para compra de apartamentos.
                        <br />
                        -84 clientes con papeles completos radicados para iniciar proceso de vinculación.
                        <br />
                        -51 clientes con crédito hipotecario aprobado en busca de documentos para completar el cierre financiero o
                        requisitos del proyecto.
                    </div>
                    </div>
                    <div id="step-14" class="tab-pane" role="tabpanel" aria-labelledby="step-14">
                    <h3>Trimestre 2</h3>
                    <p>A partir del 1º de abril de 2022, se empezó a ofertar el proyecto a toda persona que cumpla con los
                        requisitos
                        establecidos por el Gobierno Nacional para el programa “Mi Casa Ya”.
                    </p>
                    <p>
                        El 02 de mayo de 2022 el Plan de Vivienda Tunja VIP - VIS comunica que seguirá atendiendo a todas las
                        familias
                        caracterizados en el grupo 3 de priorización, hasta agotar la oferta comercial de unidades de vivienda y
                        advirtiendo que la comercialización será de carácter general y abierta a todo público.
                    </p>
                    <p>
                        El 08 de mayo del presente año, se da apertura oficial a la convocatoria para postularse al proyecto
                        Bellavista, aclarando que, desde el mes de abril la postulación está dirigida a todo público. Para esta
                        fecha,
                        195 familias ya habían iniciado los trámites correspondientes.
                    </p>
                    <p>
                        A través de una mesa de diálogo realizada el 10 de mayo del año en curso y de una visita técnica de
                        verificación de avance de obra al Proyecto Torres del Parque, la administración municipal en compañía del
                        Ministerio de Vivienda, la Unidad Nacional para la Gestión del Riesgo y Ecovivienda, atendieron los
                        requerimientos de la comunidad partiendo de la gestión adelantada ante el Gobierno Nacional para dar
                        solución
                        a los proyectos de vivienda inconclusos, concluyendo:
                        La UNGRD reconoció el riesgo de las 770 familias del grupo 2 de los proyectos de Torres del Parque y
                        Estancia
                        del Roble, que requieren reubicación.
                        Se ratificó el apoyo de la Gobernación de Boyacá por valor de 1.000 millones de pesos para la reubicación de
                        las familias.
                        Con Ministerio de Vivienda se revisaron las familias que no han podido acceder al beneficio de ‘Mi casa ya’,
                        para vinculación al proyecto BELLAVISTA.
                    </p>
                    <p>
                        El 13 de mayo de 2022 se amplió el plazo de priorización en la vinculación para las familias del grupo 3 de
                        los proyectos Torres del Parque y Estancia del Roble.
                    </p>
                    <p>
                        El 27 de mayo de 2022, como parte de su iniciativa “Caracol Radio con la Comunidad” implementada a nivel
                        nacional, la emisora Caracol Radio convocó a una actividad en el proyecto Estancia del Roble para dar
                        respuesta a las inquietudes ciudadanas por parte de la administración municipal.
                    </p>
                    <p>
                        El 23 de julio del presente año se llevó a cabo la Oferta de Servicios Bellavista dirigido a familias del
                        grupo 3 de priorización, contando con presencia del Ministerio de Vivienda, Ecovivienda, la constructora
                        Valu,
                        entidades bancarias y la participación activa de la comunidad y veedurías.
                    </p>
                    </div>
                    <div id="step-15" class="tab-pane" role="tabpanel" aria-labelledby="step-15">
                    <h3>Trimestre 3</h3>
                    <p>
                    Resolución y licencia de Construcción:
                    Viabilidad del proyecto 20 de mayo de 2022.
                    Resolución N° 0415 del 07 de julio de 2022, por la cual de concede Licencia Conjunta de Urbanismo en la
                    Modalidad de Desarrollo y de Construcción en las Modalidades de Obra Nueva y Demolición Total.
                    Licencia de Construcción e inicio de ejecución de obra – 26 de julio de 2022.
                    </p>
                    <p>
                    El 06 de agosto del año 2022 en el marco de la celebración de los 483 años de fundación hispánica de nuestra
                    ciudad de Tunja, se da apertura oficial al proyecto de vivienda de interés social y prioritario Bellavista.
                    </p>
                    <p>
                    El día 3 de septiembre de 2022 se llevó a cabo una vista técnica de seguimiento al avance del proyecto
                    Bellavista con la presencia de ECOVIVIENDA, alcalde Alejandro Fúneme, representantes del Concejo Municipal y
                    del Gabinete Municipal, en donde se evidencio un avance de ejecución del 27% de la primera etapa, en cuanto a
                    la fase de cimentación.
                    </p>
                    <p>
                    El día 15 de septiembre de 2022 tras un nuevo recorrido y seguimiento al área de intervención, se evidenció
                    que la obra alcanza un progreso del 38% con 4 torres en ejecución en su primera etapa, en las cuales se han
                    realizado actividades de cimentación, replanteo, localización, instalación de redes hidrosanitarias, obras
                    perimetrales, placas de contrapiso y muros.
                    </p>
